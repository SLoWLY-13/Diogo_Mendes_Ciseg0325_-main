# Operadores Matematicos
# + , - , / , * , // <- divisao de inteiro , ** -> expoente , % modulo devolve o resto de uma divisão

num1=0
num2=0

num1=int(input("Introduz numero 1"))
num2=int(input("Introduz numero 2"))

total=num1/num2
print("Total da divisão: ", total,)

total=num1*num2
print("Total da multiplicação: ", total,)

total=num1+num2
print("Total da soma: ", total,)

total=num1-num2
print("Total da subtração: ", total,)

total=num1//num2
print("Total da divisão por inteiro: ", total,)

total=num1**num2
print("Total da expoente: ", total,)

total=num1%num2
print("Total do modulo: ", total,)